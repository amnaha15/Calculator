
public class Basic {
	
	public static double add (double answer, double x){
		
		answer = answer + x;
		return answer;
		
	}
public static double subtract (double answer, double x){
		
		answer = answer - x;
		return answer;
		
	}
public static double multiply(double answer, double x){
	answer = answer * x;
	return answer;
}
public static double divide(double answer, double x){
	answer = answer / x;
	return answer;
}
}
